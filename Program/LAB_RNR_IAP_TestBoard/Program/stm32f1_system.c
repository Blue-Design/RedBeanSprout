/*=====================================================================================================*
**函數 : System_SetVectorTable
**功能 : 重新設定向量表
**輸入 : Offset
**輸出 : None
**使用 : System_SetVectorTable(IAP_FLASH_SIZE);
**=====================================================================================================*/
/*=====================================================================================================*/
void System_SetVectorTable( u32 Offset )
{
  SCB->VTOR = (u32)0x08000000 | (Offset & (u32)0x1FFFFF80);
//  NVIC_SetVectorTable(NVIC_VectTab_FLASH, FLASH_IAP_SIZE);
}
